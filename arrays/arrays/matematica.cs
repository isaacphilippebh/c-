using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace arrays
{
    public class Matematica
    {
        public decimal Adicao (decimal parcela1, decimal parcela2)
        {
        return parcela1 + parcela2;
        }

        public decimal Subtracao(decimal parcela1, decimal parcela2)
        {
            return parcela1 - parcela2;
        }

        public decimal Multiplicacao(decimal parcela1, decimal parcela2)
        {
            return parcela1 * parcela2;
        }

        public decimal Divisao(decimal parcela1, decimal parcela2)
        {
            return parcela1 / parcela2;
        }
    }
}
